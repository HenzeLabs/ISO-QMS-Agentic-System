CREATE OR REPLACE FUNCTION "public"."get_embedding_stats"() RETURNS TABLE("total_chunks" bigint, "chunks_with_embeddings" bigint, "embedding_coverage" numeric, "avg_content_length" numeric, "unique_documents" bigint)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    SELECT 
        COUNT(*)::BIGINT AS total_chunks,
        COUNT(embedding)::BIGINT AS chunks_with_embeddings,
        ROUND(COUNT(embedding)::NUMERIC / NULLIF(COUNT(*)::NUMERIC, 0) * 100, 2) AS embedding_coverage,
        ROUND(AVG(content_length), 2) AS avg_content_length,
        COUNT(DISTINCT document_id)::BIGINT AS unique_documents
    FROM iso_chunks;
END;
$$;


