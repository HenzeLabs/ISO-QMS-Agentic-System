CREATE OR REPLACE FUNCTION "public"."find_similar_chunks"("query_embedding" "public"."vector", "similarity_threshold" double precision DEFAULT 0.7, "doc_type_filter" "text" DEFAULT NULL::"text", "doc_category_filter" "text" DEFAULT NULL::"text", "iso_section_filter" "text" DEFAULT NULL::"text", "folder_filter" "text" DEFAULT NULL::"text", "min_content_length" integer DEFAULT 50, "max_results" integer DEFAULT 10) RETURNS TABLE("id" "uuid", "document_id" "text", "chunk_index" integer, "content" "text", "doc_type" "text", "doc_category" "text", "iso_section" "text", "folder_path" "text", "file_name" "text", "similarity" double precision, "content_length" integer, "created_at" timestamp with time zone)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    SELECT 
        c.id,
        c.document_id,
        c.chunk_index,
        c.content,
        c.doc_type,
        c.doc_category,
        c.iso_section,
        c.folder_path,
        c.file_name,
        (1 - (c.embedding <=> query_embedding))::FLOAT AS similarity,
        c.content_length,
        c.created_at
    FROM iso_chunks c
    WHERE 
        c.embedding IS NOT NULL
        AND (1 - (c.embedding <=> query_embedding)) > similarity_threshold
        AND c.content_length >= min_content_length
        AND (doc_type_filter IS NULL OR c.doc_type = doc_type_filter)
        AND (doc_category_filter IS NULL OR c.doc_category = doc_category_filter)
        AND (iso_section_filter IS NULL OR c.iso_section = iso_section_filter)
        AND (folder_filter IS NULL OR c.folder_path ILIKE '%' || folder_filter || '%')
    ORDER BY c.embedding <=> query_embedding
    LIMIT max_results;
END;
$$;


