CREATE OR REPLACE FUNCTION "public"."search_iso_documents"("query_embedding" "public"."vector", "match_threshold" double precision DEFAULT 0.5, "match_count" integer DEFAULT 10) RETURNS TABLE("document_id" "uuid", "file_name" "text", "doc_type" "text", "iso_section" "text", "chunk_index" integer, "content" "text", "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
  SELECT 
    d.id as document_id,
    d.file_name,
    d.doc_type,
    d.iso_section,
    c.chunk_index,
    c.content,
    1 - (c.embedding <=> query_embedding) as similarity
  FROM iso_document_chunks c
  JOIN iso_documents d ON c.document_id = d.id
  WHERE 1 - (c.embedding <=> query_embedding) > match_threshold
  ORDER BY c.embedding <=> query_embedding
  LIMIT match_count;
$$;


