CREATE OR REPLACE FUNCTION "public"."normalize_iso_document_ids"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    -- Ensure drive_id, file_id, and document_id stay synchronized
    NEW.file_id := COALESCE(NULLIF(NEW.file_id, ''), NEW.drive_id, NEW.document_id);
    NEW.document_id := COALESCE(NULLIF(NEW.document_id, ''), NEW.file_id, NEW.drive_id);
    NEW.drive_id := COALESCE(NULLIF(NEW.drive_id, ''), NEW.file_id, NEW.document_id);
    
    -- Ensure required fields have defaults
    NEW.bucket := COALESCE(NEW.bucket, 'default');
    NEW.export_mode := COALESCE(NEW.export_mode, 'standard');
    NEW.extracted_text := COALESCE(NEW.extracted_text, '');
    NEW.extracted_text_length := COALESCE(NEW.extracted_text_length, LENGTH(NEW.extracted_text));
    NEW.processing_timestamp := COALESCE(NEW.processing_timestamp, NOW());
    NEW.extraction_method := COALESCE(NEW.extraction_method, 'default');
    NEW.upstream_metadata := COALESCE(NEW.upstream_metadata, '{}'::jsonb);
    NEW.folder_path := COALESCE(NEW.folder_path, 'root');
    NEW.doc_type := COALESCE(NEW.doc_type, 'Other');
    NEW.doc_category := COALESCE(NEW.doc_category, 'Other');
    NEW.mime_type := COALESCE(NEW.mime_type, 'application/octet-stream');
    
    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."normalize_iso_document_ids"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."populate_iso_chunk_metadata"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    doc_row iso_documents%ROWTYPE;
BEGIN
    IF NEW.document_id IS NULL OR NEW.document_id = '' THEN
        RAISE EXCEPTION 'document_id is required for iso_chunks';
    END IF;

    SELECT * INTO doc_row FROM iso_documents WHERE document_id = NEW.document_id;

    IF FOUND THEN
        NEW.file_id := COALESCE(NULLIF(NEW.file_id, ''), doc_row.file_id);
        NEW.drive_id := COALESCE(NULLIF(NEW.drive_id, ''), doc_row.drive_id);
        NEW.file_name := COALESCE(NULLIF(NEW.file_name, ''), doc_row.file_name);
        NEW.doc_type := COALESCE(NULLIF(NEW.doc_type, ''), doc_row.doc_type);
        NEW.doc_category := COALESCE(NULLIF(NEW.doc_category, ''), doc_row.doc_category);
        NEW.iso_section := COALESCE(NEW.iso_section, doc_row.iso_section);
        NEW.folder_path := COALESCE(NULLIF(NEW.folder_path, ''), doc_row.folder_path);
        NEW.folder_level1 := COALESCE(NEW.folder_level1, doc_row.folder_level1);
        NEW.folder_level2 := COALESCE(NEW.folder_level2, doc_row.folder_level2);
        NEW.folder_level3 := COALESCE(NEW.folder_level3, doc_row.folder_level3);
        NEW.folder_level4 := COALESCE(NEW.folder_level4, doc_row.folder_level4);
        NEW.folder_level5 := COALESCE(NEW.folder_level5, doc_row.folder_level5);
        NEW.bucket := COALESCE(NEW.bucket, doc_row.bucket, 'default');
    ELSE
        NEW.file_id := COALESCE(NULLIF(NEW.file_id, ''), NEW.document_id);
        NEW.drive_id := COALESCE(NULLIF(NEW.drive_id, ''), NEW.file_id);
    END IF;

    NEW.content := COALESCE(NEW.content, '');
    NEW.content_length := COALESCE(NEW.content_length, LENGTH(NEW.content));
    NEW.processing_timestamp := COALESCE(NEW.processing_timestamp, NOW());
    NEW.upstream_metadata := COALESCE(NEW.upstream_metadata, '{}'::jsonb);
    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."populate_iso_chunk_metadata"() OWNER TO "postgres";


